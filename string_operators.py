name1 = "Josue"
name2 = "Galvez"

#Concatenation Operation
miNombre = name1 + " " + name2
print(miNombre)

#Repitition Operation
value = name2 * 5
print(value)

#Slice Operation
value = name1[3]
print(value)

#Range Slice Operation
value = name2[2:5]
print(value)

#Membership Operation
if "e" in name1:
    print("It is in the string")

#Raw Material
print(r"Hello\nThere")



