name = "Josue"
age = 40

print("Hola", name, "you are", age, "years old.")

print("Hello %s, you are %s years old." % (name,age)) #Percent % Formatting

print("Hello {0}, you are {1} years old." .format(name, age)) #STR. Formatting

print(f"Hola, {name} you are {age} years old") #F-String Formatting  (This is easier to read)

print(f"Hola, {name} in ten years you will be {age + 10} years old") #

 